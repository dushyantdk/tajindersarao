            <div class="block_title">
               <h4><strong>Step 3:</strong> Mortgage Submission and Approval</h4>
            </div>
            <p>Once you select the appropriate mortgage program, you will submit this information to your mortgage associate along with any other required documentation. You will then wait for the mortgage approval from the mortgage associate either through email or fax. After the approval, the associate will also review your commitment to the mortgage. Any additional documents that are required by the lender should be sent to the associate no later than 10 days after the approval.</p>
         </div>
         <div class="col-xl-6 col-md-6">
            <div class="block_title">
               <h4><strong>Step 4:</strong> Lawyer</h4>
            </div>
            <p>The associate will send the mortgage instructions to your lawyer to review and sign the documents. First you will review all the terms and conditions prior to signing to make sure the interest rate and loan terms are what were promised. Double check to see that the names and address are correctly spelled on the documents. Signing takes place in front of a notary public or lawyer. There will be several fees with obtaining a mortgage and transferring property ownership which will be paid at closing. Bring a bank draft check for the down payment and closing costs if required. Personal cheques are not accepted. You will also need to show homeowners insurance policy and other requirements such as flood or fire insurance and proof of payment.</p>
